<h2 class="wp-block-heading">THE PROBLEM</h2>



<p>IPLC prior had no online admission system and student Portal. They admited students through Facebook, Email and direct walk-ins. This was a challenge to the institute, as these were different platforms, they had to look into so as to process it in one spreadsheet. Apparently the process was sketchy. So they proposed to singularize the admission. Where student can admit in one platform. The “Online Admission System”. I worked on this contract to build a friendly user experience where students are presented a step-to-step process of application. This was from admission to enrollment.</p>



